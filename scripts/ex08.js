function variaveldual(){
    let a = parseInt(prompt("digite seu primeiro numero"))
    let b = parseInt(prompt("digite seu segundo numero"))

    let dif = (a - b);
    let dobro = (a + (b*3));
    let mult = (a * b);

    alert("a diferença entre os dois é de : " + dif);
    alert("O dobro da primeira variável somado ao triplo da segunda é : " + dobro);
    alert("a multiplicação das duas variaveis é : " + mult);


}